package com.cykj.bean;

public class DrivSign {
    private String DSId;
    private String DSYear;
    private String DSMonth;
    private String DSSign;
    private int DrivId;

    public DrivSign() {
    }

    public String getDSId() {
        return DSId;
    }

    public void setDSId(String DSId) {
        this.DSId = DSId;
    }

    public String getDSYear() {
        return DSYear;
    }

    public void setDSYear(String DSYear) {
        this.DSYear = DSYear;
    }

    public String getDSMonth() {
        return DSMonth;
    }

    public void setDSMonth(String DSMonth) {
        this.DSMonth = DSMonth;
    }

    public String getDSSign() {
        return DSSign;
    }

    public void setDSSign(String DSSign) {
        this.DSSign = DSSign;
    }

    public int getDrivId() {
        return DrivId;
    }

    public void setDrivId(int drivId) {
        DrivId = drivId;
    }
}
